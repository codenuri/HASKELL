f1 :: Int -> [Int]
f1 n = [0..n-1]


-- 인자로 전달된 요소의 +1, -1 값을 구하고 싶다.
f2 :: Int->[Int]
f2 n = [ n+1, n-1]


-- 4 => [0,1,2,3] => [ 1,-1, 2, 0, 3, 1, 4, 2]
